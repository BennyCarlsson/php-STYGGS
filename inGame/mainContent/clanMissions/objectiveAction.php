<?php

class objectiveAction {
	public $ActionId;
	public $ClanId;
	public $DefendingUnits;
	public $AttackingUnits;
	public $ClanObjectiveId;
	
	public function __construct($actionId, $clanId, $defendingUnits, $attackingUnits, $clanobjectiveId){
		$this -> ActionId = $actionId;
		$this -> ClanId = $clanId;
		$this -> DefendingUnits = $defendingUnits;
		$this -> AttackingUnits = $attackingUnits;
		$this -> ClanObjectiveId = $clanobjectiveId;
	}
}
